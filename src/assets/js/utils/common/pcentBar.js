export function drawPcentBar(g,pos,glyphWidth,glyphHeight,cellHeight,xPercent,yPercent){
    g.append('rect')
    .attr('x',pos[0] + glyphWidth - cellHeight / 8)
    .attr('y',pos[1] + cellHeight)
    .attr('width',cellHeight / 4)
    .attr('height',glyphHeight - cellHeight + cellHeight / 8)
    .attr('fill','white')
    // .attr('opacity',0.5)
    .attr('stroke-width',"0.5px")
    .attr('stroke','black')

    g.append('rect')
    .attr('x',pos[0] + glyphWidth - cellHeight / 8)
    .attr('y',pos[1] + cellHeight)
    .attr('width',cellHeight / 4)
    .attr('height',yPercent * (glyphHeight - cellHeight) + cellHeight / 8)
    .attr('fill','gray')

    g.append('rect')
    .attr('x',pos[0])
    .attr('y',pos[1] + glyphHeight - cellHeight / 8)
    .attr('width',glyphWidth - cellHeight / 8)
    .attr('height',cellHeight / 4)
    .attr('fill','white')
    // .attr('opacity',0.5)
    .attr('stroke-width',"0.5px")
    .attr('stroke','black')

    g.append('rect')
    .attr('x',pos[0])
    .attr('y',pos[1] + glyphHeight - cellHeight / 8)
    .attr('width',xPercent * glyphWidth - cellHeight / 8)
    .attr('height',cellHeight / 4)
    .attr('fill','gray')
}